def presenteer(mijn_dict, totaal):
    for key, value in mijn_dict.items():
        print(f"{key} : {value} euro")
    
    print("==========================")
    print(f"totaal : {totaal} euro")

mijn_dictionary = {'vis' : 10, 'vlees': 25, 'overig' : 15}
totaal = 50
presenteer(mijn_dictionary, totaal)